def default_config(
    *,
    profile: str = "minimal",
    port: int = 8888,
    gpu: bool | str = "auto",
    image: str | None = None,
) -> dict[str, Any]:
    data = deepcopy(DEFAULT_CONFIG)
    data["workspace"]["profile"] = profile
    data["workspace"]["port"] = port
    data["workspace"]["gpu"] = gpu
    if image is not None:
        data["workspace"]["image"] = image
    return data


def merge_defaults(data: dict[str, Any]) -> dict[str, Any]:
    merged = deepcopy(DEFAULT_CONFIG)
    for section, values in data.items():
        if isinstance(values, dict) and isinstance(merged.get(section), dict):
            merged[section].update(values)
        else:
            merged[section] = values
    return merged


def read_config(path: Path) -> dict[str, Any]:
    if not path.is_file():
        raise ConfigError(f"Config file not found: {path}")
    try:
        if path.suffix.lower() in {".yml", ".yaml"}:
            loaded = yaml.safe_load(path.read_text()) or {}
        else:
            loaded = tomllib.loads(path.read_text())
    except tomllib.TOMLDecodeError as exc:
        raise ConfigError(f"Invalid TOML in {path}: {exc}") from exc
    except yaml.YAMLError as exc:
        raise ConfigError(f"Invalid YAML in {path}: {exc}") from exc
    if not isinstance(loaded, dict):
        raise ConfigError(f"Config file must contain a mapping: {path}")
    data = dict(loaded)
    return validate_config(merge_defaults(data))


def write_config(
    path: Path,
    data: dict[str, Any],
    *,
    overwrite: bool = False,
    documented: bool = False,
) -> None:
    if path.exists() and not overwrite:
        raise ConfigError(f"{path.name} already exists. Use --force to overwrite it.")
    if path.suffix.lower() not in {".yml", ".yaml"}:
        raise ConfigError("New workspace configuration must be written as dsml.yml.")
    validated = validate_config(merge_defaults(data))
    path.write_text(render_documented_config(validated) if documented else render_config(validated))


def render_config(data: dict[str, Any]) -> str:
    validated = validate_config(merge_defaults(data))
    return yaml.dump(validated, Dumper=_IndentedDumper, sort_keys=False, default_flow_style=False)


