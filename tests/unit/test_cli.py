def test_status_command_renders_backend_status(monkeypatch, tmp_path):
    monkeypatch.setattr(
        cli.runtime,
        "status",
        lambda: cli.runtime.WorkspaceStatus(
            backend="compose",
            project_name="project-test",
            container_name="dsml-project-test",
            image="example:test",
            running=True,
            config_path=tmp_path / "dsml.toml",
            compose_file=tmp_path / ".dsml" / "compose.yaml",
            url="http://127.0.0.1:8888/?token=test",
        ),
    )

    result = runner.invoke(app, ["status"])

    assert result.exit_code == 0
    assert "compose" in result.stdout
    assert "project-test" in result.stdout


def test_compose_debug_commands_call_runtime(monkeypatch):
    calls = []

    monkeypatch.setattr(cli.runtime, "compose_path_for_workspace", lambda: Path(".dsml/compose.yaml"))
    monkeypatch.setattr(cli.runtime, "compose_config", lambda: calls.append("config"))
    monkeypatch.setattr(cli.runtime, "compose_ps", lambda: calls.append("ps"))

    path_result = runner.invoke(app, ["compose", "path"])
    config_result = runner.invoke(app, ["compose", "config"])
    ps_result = runner.invoke(app, ["compose", "ps"])

    assert path_result.exit_code == 0
    assert ".dsml/compose.yaml" in path_result.stdout
    assert config_result.exit_code == 0
    assert ps_result.exit_code == 0
    assert calls == ["config", "ps"]
